<div class="title">CARE ESTE SCOPUL B2B?</div>

<p>
	<strong>B2B</strong> pune la dispoziție informații legate de consumator.
</p>

<p>
	<strong>B2B</strong> pune la dispoziție informații noi legate de sistemele de automatizare dintre Auger Autotechnik SRL și sistemele automatizate ale consumatorului.
</p>

<p>
	<strong>B2B</strong> înlesnește aflarea informațiilor referitoare la codurile produselor, prețurilor și a disponibilității în stoc și astfel consumatorul poate corecta în sistemele proprii informațiile despre produse.
</p>

<p>
	<strong>B2B</strong> înlesnește aflarea informațiilor despre stoc, comenzi, comenzi aflate în așteptare și situația plăților.
</p>