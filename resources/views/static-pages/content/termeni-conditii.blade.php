<p>i. Piesa a fost deteriorata datorita exploatarii necorespunzatoare a vehiculului prin: neefectuarea rodajelor, a turatiei maxime admise, depasirea parametrilor de lucru a sistemelor, supraincarcarilor, a neglijentei, a proastei intretineri sau a reglajelor/verificarilor/reviziilor tehnice periodice necorespunzatoare.</p>
