<h1>Modalitati de remediere a lipsei de conformitate:</h1>
<h2>A. Inlocuirea produsului</h2>
<p>
	Inlocuirea  produselor care prezinta defecte se va realiza numai in cazul in care aceasta masura este posibila de catre furnizor, in functie de costurile necesare si de stocul existent al produsului respectiv. Daca inlocuirea este imposibila, atunci se va gasi o alta solutie de remediere cu acordul ambelor parti.
</p>
<p>
	Remedierea neconformitatii produselor va fi solutionata intr-un termen rezonabil stabilit de comun acord intre furnizor si cumparator.
</p>
<p>
	Pentru inlocuirea unei piese in termen de garantie, solicitantul va prezenta urmatoarele documente:
</p>
<ol>
<li>Factura de cumparare a piesei emisa de AUGER AUTOTECHNIK SRL.</li>
<li>Dovada platii piesei respective.</li>
<li>Certificatul de garantie semnat de furnizor .</li>
<li>Copie dupa certificatul de inmatriculare sau cartea de identitate a autovehiculului al carei piese fac obiectul garantiei.</li>
<li>Dovada montarii piesei intr-un atelier de specialitate, autorizat de producator sau acreditat RAR (bon de lucru/deviz montaj), pentru tipul de operatiune/lucrare, precum si documentele fiscale aferente emise de unitatea de service auto.</li>
<li>Nota de constatare a lipsei de conformitate sau a viciului ascuns al piesei, intocmita de atelierul care a efectuat montarea.</li>
<li>Piesa trebuie sa fie in ambalajul original.</li>
</ol>

<h2>B. Cumparatorul  poate solicita o reducere corespunzatoare a pretului sau rezilierea acordurilor anterioare (contract de vanzare-cumparare):</h2>
<p>a. daca nu beneficiaza nici de repararea, nici de inlocuirea produsului;</p>
<p>b. daca vanzatorul nu a luat masura reparatorie, in perioada de timp rezonabila care a fost stabilita de comun acord de catre ambele parti;</p>
<p>c. daca vanzatorul nu a luat masura reparatorie, atunci cand inlocuirea produsului defect a fost imposibila datorata stocului indisponibil al produsului respectiv.</p>
<br/>
<p>Cumparatorul nu este indreptatit sa solicite rezilierea acordurilor incheiate anterior (contract de vanzare-cumparare, factura), daca defectul este minor; gradul de gravitate al defectului va fi stabilit conform prevederilor standardelor internationale si ale actelor normative in vigoare.</p>

