<div class="title">CUM PUTEȚI UTILIZA SISTEMUL B2B OFERIT DE AUGER AUTOTECHNIK SRL?</div>

<p>
	Auger Autotechnik SRL pune la dispoziția clienților săi opțiunea de a comanda și verifica on-line disponibilitatea produselor. Puteți verifica facturile, comenzile în așteptare, comenzile încheiate, registrul financiar prin intermediul platformei B2B.
</p>

<p>
	Mai mult chiar, puteți descărca cataloagele noastre cu produse noi și existente și puteți fi la curent cu situația stocurilor, a cantităților existente și prețurilor produselor.
</p>

<p>
	Puteți crea o ofertă pentru produsele pe care le doriți, selectându-le pe rând sau încărcându-le la un loc, sub forma unui singur document. Oferta va fi confirmată de un reprezentat de vânzări și apoi va fi înregistrată în sistem drept comandă.
</p>